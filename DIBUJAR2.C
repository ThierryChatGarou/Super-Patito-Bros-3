void obtener_segundos()
  {
  gettime(&hora);
  ultimosegundo=hora.ti_sec;
  segundo_invalido=1;
  FPS=0;
  }


void ajuste_FPS()
  {
  gettime(&hora);
  FPS++;
  if(ultimosegundo != hora.ti_sec) 
    {
    ultimosegundo=hora.ti_sec;
    if(segundo_invalido==1)  //cuando se inicia el juego el primer segundo puede venir incompleto, por lo que se ignora el primer segundo
      {
      segundo_invalido=0;
      }
    else
      {
      if(FPS<CPS-2)
        {
        if(retraso>1)
          {
          retraso--;
          }
        }
      if(FPS>CPS+2)
        {
        if(retraso<48)
          {
          retraso++;
          }
        }
      if(FPS<CPS-4)
        {
        if(retraso>2)
          {
          retraso=retraso-2;
          }
        }
      if(FPS>CPS+4)
        {
        if(retraso<47)
          {
          retraso=retraso+2;
          }
        }
      if(debug)
        {
        gotoxy(4,30), printf("FPS=%d ",FPS);
        }
      }
    FPS=0;
    }
  }


